'use strict';

let days = 7;
console.log('days =', days, "\n");

// Equality Test Operator
console.log("days == '7'", days == '7');    // Auto Converted to number.

// Identity Test Operator
console.log("days === '7'", days === '7');  // No Conversion; Much Stricter; Test Both Equality & Type

console.log("days != '7'", days != '7');
console.log("days !== '7'", days !== '7');  



