'use strict';

let text = 'cat';

console.log('text =', text, "\n");

console.log("text == 'cat'", text == 'cat');        // Equality
console.log("text === 'cat'", text === 'cat');      // Identity; Preferred

console.log("text != 'cat'", text != 'cat');
console.log("text !== 'cat'", text !== 'cat');

console.log("text < 'dog'", text < 'dog');
console.log("text <= 'dog'", text <= 'dog');

console.log("text > 'dog'", text > 'dog');
console.log("text >= 'dog'", text >= 'dog');
console.log("'Dog' >= 'dog'", 'Dog' >= 'dog');
